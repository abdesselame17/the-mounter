    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        // save Selected Data if not changed.
        int[] selectedDataIndex = new int[11];
        JComboBox[] selectedDataType = {comboCpu, comboCM, comboRam, comboCG, comboDisque, comboDisque2, comboVentillo, comboLecteur, comboBoitier, comboAlimentation, comboEcran};
        for(int i = 0; i < selectedDataIndex.length; i++){
            selectedDataIndex[i] = selectedDataType[i].getSelectedIndex();
        }
        new Articles(this).setVisible(true);
        // Load the new Data.
        loadComboValues();
        // Restore selected data.
        for(int i = 0; i < selectedDataIndex.length; i++){
            selectedDataType[i].setSelectedIndex(selectedDataIndex[i]);
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

