    public void updateMergeValue(){
        updateRawTotalValue();
        int mv = (this.rawTotalValue * this.merge.getValue()) / 100;
        mergeValue.setText(String.valueOf(mv));
        updateTotalValue();
    }
   
    public void updateRawTotalValue(){
        rawTotalValue = Integer.valueOf(cpuValue.getText())+
                Integer.valueOf(cmValue.getText())+
                Integer.valueOf(ramValue.getText())+
                Integer.valueOf(disqueValue.getText())+
                Integer.valueOf(disk2Value.getText())+
                Integer.valueOf(cgValue.getText())+
                Integer.valueOf(ventilloValue.getText())+
                Integer.valueOf(lecteurValue.getText())+
                Integer.valueOf(boitierValue.getText())+
                Integer.valueOf(alimentationValue.getText())+
                Integer.valueOf(ecranValue.getText());
    }
    
    public void updateTotalValue(){
        totalValue.setText(Integer.toString(Integer.valueOf(mergeValue.getText())+rawTotalValue));    
    }
    
    public void valueFocusLost(JTextField txtField, JComboBox combo){
        try {
            Integer.parseInt(txtField.getText());
            updateMergeValue();
        } catch (NumberFormatException e) {
            txtField.setText(new MyConnection().getValue(combo.getSelectedItem().toString(), comboPrix.getSelectedItem().toString()));
            updateMergeValue();
        }
    }
    
    public void valueKeyPressed(KeyEvent evt, JTextField txtField, JTextField nextTxtField){
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            try {
                Integer.parseInt(txtField.getText());
                updateMergeValue();
                nextTxtField.requestFocusInWindow();
            } catch (NumberFormatException e) {
                
            }
        }
    }
    
